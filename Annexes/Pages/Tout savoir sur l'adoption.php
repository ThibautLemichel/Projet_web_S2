                <p>Il est vivement conseillé de prendre un chiot après l’âge de 8 semaines car un sevrage trop précoce peut avoir des conséquences sur le développement du chiot et certaines difficultés peuvent survenir quelques semaines plus tard ou à l’adolescence.</p>
