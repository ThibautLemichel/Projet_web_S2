                <p>Sur le plan médical, les futurs maîtres ne pourront qu’être satisfaits et éviteront les mauvaises surprises. Les chiens en refuge bénéficient d’un suivi médical régulier, les vaccins sont à jour, l’identification est faite dans les normes. Ils ont l’assurance d’adopter un animal en bonne santé. Si ce chien souffre d’un quelconque handicap, la transparence sera de mise et les adoptants seront libres de leur décision.</p>
