                <p>Accueillir un nouveau chien dans son foyer est un grand moment de bonheur. Cependant, cela représente aussi certaines responsabilités et nécessite donc des préparations</p>
                <h2>Quels accessoires faut-il préparer pour accueillir un chiot dans de bonnes conditions ?</h2>
                <ul>
                    <li>Le couchage : rendez ses nuits confortables. Préparez-lui un couchage doux, à sa taille d'adulte et lavable dans lequel il puisse se blottir. Apprenez à votre chiot à dormir dans un panier pour chien ou à s'installer dans une caisse pour la nuit.</li>
                    <li>Les repas : vous aurez besoin de deux gamelles (pour la nourriture et pour l'eau fraîche). Préférez-les en métal ou en faïence, plus solides et ne donnant pas de goùt aux aliments.</li>
                    <li>Le collier : un bon collier doit être ajusté mais un peu lâche, sans être trop large pour éviter qu'il ne passe par-dessus la tête. Vous devez pouvoir passer deux doigts entre le cou et le collier. Par ailleurs, vérifiez la taille du collier chaque semaine pendant la croissance de votre chiot, en le desserrant lorsque c'est nécessaire.</li>
                    <li>L'identification : munissez le collier d'une plaque d'identité avec des informations à jour et provoyez avec votre vétérinaire l'insertion d'une micro puce d'identification pour le cas où votre chien se perdrait ou que vous voyagiez avec lui.</li>
                    <li>La brosse et l'étrille : brosser son chien régulièrement est essentiel, surtout s'il s'agit d'un chien à poils longs. Choisissez une brosse et une étrille adaptée au pelage de votre chien. Initiez votre chiot aux plaisirs du toilettage en le brossant tous les jours et en lui offrant une petite friandise après la séance.</li>
                    <li>La brosse à dents : commencez à prendre soin de ses dents de bonne heure. Choisissez une brosse à dents spécifique pour chiens et un dentifrice canin aux enzymes.</li>
                    <li>Le shampooing : les chiens peuvent avoir besoin d'un bain de temps en temps, votre vétérinaire vous recommandera un shampooing doux pour chien.</li>
                    <li>Le coupe-ongles : choisissez-en un conçu pour les chiens. Ne coupez pas trop souvent les ongles du chien, uniquement quand ils sont trop longs. Habituez votre chiot à ce qu'on manipule ses pattes très tôt, ce sera utile dans le futur.</li>
                    <li>Des jouets et des jeux : vérifiez qu'ils sont suffisamment grands et solides pour éviter que votre chiot ne les avale. Rangez-les entre chaque séance pour ne pas que votre chiot s'en lasse. En plus d'une fonction ludique, les jouets ont une utilité : les chiots mâchent pour découvrir les nouveaux objets et perdre leurs dents de lait. Vous éviterez qu'il ne s'attaque à vos meubles, à vos chaussures ou à vos télécommandes. Jouer à rapporter des balles et des anneaux est aussi un bon exercice physique et d'éducation pour votre chiot.</li>
                </ul>
